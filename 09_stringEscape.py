# \n : 줄바꿈
print("백문이 불여일견 \n백견이 불여일타")

# 저는 "아무개" 입니다.
print("저는 '아무개'입니다.")
print('저는 "아무개"입니다.')
print("저는 \"아무개\"입니다.")
# "" 이거 앞에 \ 를 붙어야 한다. 앞에

# \\ : 문장 내에서 \
print("컴퓨터\\씨\\내폴더\\파이썬")

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine")
# Red Apple 을 쓰고 젤 앞으로 이동 후 덮어쓴다.

# \b : 백스페이스
print("Redd\b Apple")

# \t : tab
print("Redd\t Apple")
